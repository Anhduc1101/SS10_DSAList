package daoNguocListSoNguyen;

public class ReverseArray {
    private int a;

    public ReverseArray() {
    }

    public ReverseArray(int a) {
        this.a = a;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    @Override
    public String toString() {
        return "IntegerArray{" +
                "a=" + a +
                '}';
    }
}
