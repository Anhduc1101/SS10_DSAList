package cacHamTrongList;

public class IntegerArray {
    private int a;

    public IntegerArray() {
    }

    public IntegerArray(int a) {
        this.a = a;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    @Override
    public String toString() {
        return "IntegerArray{" +
                "a=" + a +
                '}';
    }
}
